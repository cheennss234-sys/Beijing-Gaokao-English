# ============================================================
# Rule-based POS detection (suffix heuristics)
# ============================================================

NOUN_SUFFIXES = ('tion', 'ment', 'ness', 'ity', 'ance', 'ence',
                 'er', 'or', 'ist', 'ism')
ADJ_SUFFIXES = ('ful', 'less', 'ous', 'ive', 'able', 'ible',
                'al', 'ic', 'ish', 'ant', 'ent')
ADV_SUFFIXES = ('ly',)
VERB_SUFFIXES = ('ize', 'ise', 'ify', 'ate', 'en')


def detect_pos_by_rules(word):
    """Return a POS string based on suffix heuristics, or '' if no match."""
    w = word.lower()
    # Order matters: check longer/more-specific suffixes first.
    for suf in NOUN_SUFFIXES:
        if w.endswith(suf) and len(w) > len(suf):
            return 'n.'
    for suf in ADJ_SUFFIXES:
        if w.endswith(suf) and len(w) > len(suf):
            return 'adj.'
    for suf in VERB_SUFFIXES:
        if w.endswith(suf) and len(w) > len(suf):
            return 'v.'
    for suf in ADV_SUFFIXES:
        if w.endswith(suf) and len(w) > len(suf):
            return 'adv.'
    return ''


