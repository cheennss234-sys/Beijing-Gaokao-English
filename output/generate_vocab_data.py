INPUT_PATH = '/workspace/output/exam_texts/word_freq_raw.txt'
OUTPUT_PATH = '/workspace/output/vocab_data.json'


def read_word_freq(path):
    """Read word\tfrequency file, return list of (word, freq) preserving order."""
    entries = []
    with open(path, 'r', encoding='utf-8') as f:
        for line in f:
            line = line.rstrip('\n')
            if not line:
                continue
            parts = line.split('\t')
            if len(parts) != 2:
                continue
            word, freq_str = parts
            try:
                freq = int(freq_str)
            except ValueError:
                continue
            entries.append((word, freq))
    return entries


def build_vocab_data(entries):
    """Build the words list and compute statistics."""
    words_out = []
    dict_count = 0
    rule_count = 0

    for rank, (word, freq) in enumerate(entries, start=1):
        if word in ENGLISH_VOCAB:
            pos, meaning = ENGLISH_VOCAB[word]
            dict_count += 1
        else:
            pos = detect_pos_by_rules(word)
            meaning = ''
            if pos:
                rule_count += 1
        words_out.append({
            "rank": rank,
            "word": word,
            "pos": pos,
            "meaning": meaning,
            "frequency": freq,
        })

    return words_out, dict_count, rule_count


def main():
    entries = read_word_freq(INPUT_PATH)
    words_out, dict_count, rule_count = build_vocab_data(entries)
    total_words = len(words_out)

    data = {
        "metadata": {
            "title": "北京高三英语课外词汇频率表",
            "description": "基于2024-2026年北京高三英语试卷的课外词汇频率分析",
            "total_words": total_words,
            "total_papers": 131,
            "frequency_threshold": ">=2",
            "generated_at": str(date.today()),
        },
        "words": words_out,
    }

    with open(OUTPUT_PATH, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    # Report
    print(f"Read {len(entries)} word entries from {INPUT_PATH}")
    print(f"Dictionary hits (POS + meaning): {dict_count}")
    print(f"Rule-based POS detection: {rule_count}")
    print(f"No POS detected: {total_words - dict_count - rule_count}")
    print(f"Total words in output: {total_words}")
    print(f"Dictionary size: {len(ENGLISH_VOCAB)} entries")
    print(f"Output written to {OUTPUT_PATH}")
    print("\nFirst 10 entries:")
    for w in words_out[:10]:
        print(f"  {w['rank']}. {w['word']} | {w['pos']} | {w['meaning']} | freq={w['frequency']}")


if __name__ == '__main__':
    main()
